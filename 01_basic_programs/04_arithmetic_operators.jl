# Program to demonstrate arithmetic operators in Julia

# Declare two variables
a = 10
b = 5

# Perform arithmetic operations
sum_result = a + b
difference_result = a - b
product_result = a * b
quotient_result = a / b

# Print the results
println("Sum: ", sum_result)
println("Difference: ", difference_result)
println("Product: ", product_result)
println("Quotient: ", quotient_result)

# Output:
# Sum: 15
# Difference: 5
# Product: 50
# Quotient: 2.0
