# Program to demonstrate rational and complex numbers in Julia

# Declare a rational number
rational_num = 3//4  # 3 divided by 4

# Declare a complex number
complex_num = 3 + 4im  # 3 + 4i

# Print both numbers
println("Rational Number: ", rational_num)
println("Complex Number: ", complex_num)

# Output:
# Rational Number: 3//4
# Complex Number: 3 + 4im
