# Program to demonstrate variables and comments in Julia

# Declare a variable
x = 5

# Print the value of x
println("The value of x is: ", x)

# A comment explaining that the next line adds 2 to x
y = x + 2

# Print the updated value of y
println("The value of y (x + 2) is: ", y)

# Output:
# The value of x is: 5
# The value of y (x + 2) is: 7
