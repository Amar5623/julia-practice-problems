# Program to demonstrate numeric comparison operators in Julia

# Declare two variables
x = 10
y = 5

# Perform comparison operations
equal_result = x == y       # Check if x is equal to y
not_equal_result = x != y   # Check if x is not equal to y
greater_than_result = x > y # Check if x is greater than y
less_than_result = x < y    # Check if x is less than y

# Print the results
println("x == y: ", equal_result)
println("x != y: ", not_equal_result)
println("x > y: ", greater_than_result)
println("x < y: ", less_than_result)

# Output:
# x == y: false
# x != y: true
# x > y: true
# x < y: false
