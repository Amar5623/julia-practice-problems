# Program to demonstrate hyperbolic functions

# Define a number
x = 1

# Calculate the hyperbolic sine, cosine, and tangent
sinh_val = sinh(x)
cosh_val = cosh(x)
tanh_val = tanh(x)

# Print the results
println("Hyperbolic Sine of ", x, ": ", sinh_val)
println("Hyperbolic Cosine of ", x, ": ", cosh_val)
println("Hyperbolic Tangent of ", x, ": ", tanh_val)

# Output:
# Hyperbolic Sine of 1: 1.1752011936438014
# Hyperbolic Cosine of 1: 1.5430806348152437
# Hyperbolic Tangent of 1: 0.7615941559557649
