# Program to demonstrate division and sign functions

# Define two numbers
a = 7
b = 3

# Perform division
div_result = a / b

# Find the sign of a number
sign_result = sign(-a)

# Print the results
println("Division Result: ", div_result)
println("Sign of -a: ", sign_result)

# Output:
# Division Result: 2.3333333333333335
# Sign of -a: -1
