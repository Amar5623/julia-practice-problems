# Program to demonstrate square root and cube root functions

# Define a number
num = 16

# Calculate the square root and cube root
sqrt_result = sqrt(num)
cbrt_result = cbrt(num)

# Print the results
println("Square Root of ", num, ": ", sqrt_result)
println("Cube Root of ", num, ": ", cbrt_result)

# Output:
# Square Root of 16: 4.0
# Cube Root of 16: 2.51984220843922
